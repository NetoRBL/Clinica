    private void btnEditaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditaActionPerformed
        // TODO add your handling code here:
        SecretariaModel secModel = new SecretariaModel();
        
        secModel.setCod(cod.getText());
        secModel.setRg(rg.getText());
        secModel.setCpf(cpf.getText());
        secModel.setEndereco(endereco.getText());
        secModel.setNome(Nome.getText());
        secModel.setSexo((String) sexo.getSelectedItem());
        
        DefaultTableModel tblSec = (DefaultTableModel) tblSecretarias.getModel();
        try {
            // TODO add your handling code here:    
            SecretariaDAO secDAO = new SecretariaDAO();
            if(tblSecretarias.getSelectedRow() != -1){
                tblSecretarias.setValueAt(cod.getText(), tblSecretarias.getSelectedRow(), 0);
                tblSecretarias.setValueAt(rg.getText(), tblSecretarias.getSelectedRow(), 1);
                tblSecretarias.setValueAt(Nome.getText(), tblSecretarias.getSelectedRow(), 2);
                tblSecretarias.setValueAt(cpf.getText(), tblSecretarias.getSelectedRow(), 3);
                tblSecretarias.setValueAt(endereco.getText(), tblSecretarias.getSelectedRow(), 4);
                tblSecretarias.setValueAt(sexo.getSelectedItem(), tblSecretarias.getSelectedRow(), 5);
                secDAO.editarSecretaria(secModel);
                JOptionPane.showMessageDialog(null, "Secretária editada com sucesso!");
            }else{
                JOptionPane.showMessageDialog(null, "Selecione a secretária antes de editar!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(telaAdministrador.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEditaActionPerformed

    private void btnApagaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagaActionPerformed
        SecretariaModel secModel = new SecretariaModel();
        secModel.setCod(cod.getText());
        DefaultTableModel tblSec = (DefaultTableModel) tblSecretarias.getModel();
        try {
            // TODO add your handling code here:    
            SecretariaDAO secDAO = new SecretariaDAO();
            if(tblSecretarias.getSelectedRow() != -1){
                secDAO.apagarSecretaria(secModel);
                tblSec.removeRow(tblSecretarias.getSelectedRow());
                JOptionPane.showMessageDialog(null, "Secretária deletada com sucesso!");
            }else{
                JOptionPane.showMessageDialog(null, "Selecione a secretária antes de apagar!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(telaAdministrador.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnApagaActionPerformed

