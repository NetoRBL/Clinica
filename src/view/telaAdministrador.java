    private void btnCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroActionPerformed
        // TODO add your handling code here:
        SecretariaModel secretaria = new SecretariaModel();
        SecretariaDAO secDAO = null;
        try {
            secDAO = new SecretariaDAO();
        } catch (SQLException ex) {
            Logger.getLogger(telaAdministrador.class.getName()).log(Level.SEVERE, null, ex);
        }
        List<SecretariaModel> secs = new ArrayList<>();
        
        AdministradorDAO adminDAO = null;
        try {
            adminDAO = new AdministradorDAO();
        } catch (SQLException ex) {
            Logger.getLogger(telaAdministrador.class.getName()).log(Level.SEVERE, null, ex);
        }
        secretaria.setNome(Nome.getText());
        secretaria.setCpf(cpf.getText());
        secretaria.setEndereco(endereco.getText());
        secretaria.setLogin(login.getText());
        secretaria.setRg(rg.getText());
        secretaria.setSenha(senha.getText());
        secretaria.setSexo((String) sexo.getSelectedItem());
        
        try {
            secs = secDAO.listarSecretarias();
        } catch (SQLException ex) {
            Logger.getLogger(telaAdministrador.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            if(senha.getText().equals(confirmSenha.getText())){
                adminDAO.cadastrarSecretarias(secretaria);
                JOptionPane.showMessageDialog(null, "Cadastro efetuado com sucesso!");
                cadastraSecretariaTabela();
            }else {
                JOptionPane.showMessageDialog(null, "A senha e confirmação de senha precisam ser iguais!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(telaAdministrador.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_btnCadastroActionPerformed

