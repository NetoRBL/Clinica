    public void preencheTabela() throws SQLException{
        SecretariaDAO sdao = null;
        DefaultTableModel tblSec = (DefaultTableModel) tblSecretarias.getModel();
        
        try {
            sdao = new SecretariaDAO();
            for(SecretariaModel s : sdao.listarSecretarias()){
                Object [] dados = {s.getCod(),s.getRg(),s.getNome(),s.getCpf(),s.getEndereco(),s.getSexo()};
                tblSec.addRow(dados);
            }
        } catch (SQLException ex) {
            Logger.getLogger(telaAdministrador.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }
    
    
